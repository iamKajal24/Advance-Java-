package basicJava;

public class Conversion_And_Casting {
	public static void main(String args[]) {
		float f =5.6f;
		int x = (int)f;
		System.out.println(x);
		
		int a = 12;
		byte k = (byte)a;
		System.out.println(k);
		
		byte a1 = 10;
		byte b1 = 30;
		int result = a1*b1;
		System.out.println(result);
		
		
	}
}
